void solve() {
    ll n; cin >> n;

    vi a (n); for (ll i = 0; i < n; i++) cin >> a[i]; sort(all(a));

    vi diff (n - 1); for (ll i = 0; i < n - 1; i++) diff[i] = a[i + 1] - a[i];

    ll val = diff[0], res = inf; for (ll i = 2; i < n - 1; i++) res = min(res, diff[i] + val), val = min(val, diff[i - 1]);

    cout << res << "\n";
}

