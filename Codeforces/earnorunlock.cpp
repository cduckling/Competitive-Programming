void solve() {
    ll n; cin >> n;

    vi a (n);

    bitset<200000> dp (1);

    ll sum = 0, ans = 0;

    for (ll i = 0; i < n * 2; i++) {
        if (i < n) cin >> a[i], sum += a[i];
        if (dp[i]) ans = max(ans, sum - i);
        if (i < n) dp |= dp<<a[i], dp[i] = false;
    }

    cout << ans << "\n";
}

